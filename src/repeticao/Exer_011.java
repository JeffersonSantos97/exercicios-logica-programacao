package repeticao;

import java.util.Scanner;

public class Exer_011 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);

        int numero = sc.nextInt();

        for(int i = 1; i <= numero ; i++ ) {

            System.out.print( i + " ");
        }






        sc.close();
    }
}
